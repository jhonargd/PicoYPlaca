package com.co.model;

public class Registro {

    String placa;
    String fache;
    String contravencion;

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getFache() {
        return fache;
    }

    public void setFache(String fache) {
        this.fache = fache;
    }

    public String getContravencion() {
        return contravencion;
    }

    public void setContravencion(String contravencion) {
        this.contravencion = contravencion;
    }
}
